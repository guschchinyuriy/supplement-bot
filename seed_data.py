def seed():
    init_db()
    conn = get_connection()
    cursor = conn.cursor()

    cursor.execute("DELETE FROM supplements")
    cursor.execute("DELETE FROM symptom_cards")

    for s in SUPPLEMENTS:
        cursor.execute("""
            INSERT INTO supplements (name, name_en, category, description, ingredients,
                                    benefits, dosage, available_ru, sanctions_note)
            VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)
        """, (s["name"], s["name_en"], s["category"], s["description"],
              s["ingredients"], s["benefits"], s["dosage"],
              s["available_ru"], s["sanctions_note"]))

    for card in SYMPTOM_CARDS:
        cursor.execute("""
            INSERT INTO symptom_cards (symptom, emoji, causes, advice, supplement_names)
            VALUES (?, ?, ?, ?, ?)
        """, (card["symptom"], card["emoji"], card["causes"],
              card["advice"], card["supplement_names"]))

    conn.commit()
    conn.close()
    print(f"✅ Добавлено {len(SUPPLEMENTS)} добавок и {len(SYMPTOM_CARDS)} симптом-карточек")


