"""
Модуль проверки доступности добавок и брендов в России.
Обновляй RESTRICTED и DISCONTINUED по мере изменений на рынке.
"""

# Бренды с ограниченной доступностью после 2022
RESTRICTED = {
    "now foods": "Официальные поставки прекращены. Доступно через параллельный импорт на Ozon/Wildberries, но цены выросли в 2–3 раза.",
    "solgar": "Официальный дистрибьютор ушёл с рынка РФ. Находится в аптеках по остаткам, цены высокие.",
    "thorne": "Недоступен в России официально. Можно заказать через посредников из США.",
    "life extension": "Ограниченные поставки. Ищи на iHerb с доставкой через посредника.",
    "garden of life": "Официальные поставки прекращены. Есть остатки на маркетплейсах.",
    "jarrow formulas": "Частично доступен через параллельный импорт.",
    "nordic naturals": "Официально недоступен. Параллельный импорт.",
    "pure encapsulations": "Недоступен официально в РФ.",
    "doctor's best": "Ограниченная доступность, только параллельный импорт.",
    "natrol": "Частично доступен в аптеках.",
}

# Конкретные вещества/препараты под ограничением
DISCONTINUED = {
    "мелатонин": None,  # доступен, особых ограничений нет
}

# Бренды/продукты которые точно доступны
AVAILABLE_RU = [
    "эвалар",
    "solgar натуралис",
    "vplab",
    "olimp",
    "now",        # Now Sports (другой бренд) частично есть
    "доппельгерц",
    "алфавит",
    "компливит",
    "витрум",
    "супрадин",
    "мульти-табс",
]


def check_availability(query: str) -> dict:
    """
    Проверяет доступность бренда/продукта в России.
    Возвращает словарь: {"available": bool, "note": str | None}
    """
    q = query.lower().strip()

    # Проверяем заведомо доступные бренды
    for brand in AVAILABLE_RU:
        if brand in q:
            return {
                "available": True,
                "note": f"Бренд «{query}» широко представлен в российских аптеках и маркетплейсах."
            }

    # Проверяем ограниченные бренды
    for brand, note in RESTRICTED.items():
        if brand in q or q in brand:
            return {
                "available": False,
                "note": note
            }

    # Если не нашли — считаем доступным, но с оговоркой
    return {
        "available": True,
        "note": None
    }
